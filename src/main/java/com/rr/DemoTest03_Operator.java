package com.rr;

/**
 * 算数运算符
 * 
 * +	加
 * -	减
 * *	乘
 * /	除
 * %	求余
 * @author Administrator
 *
 */
public class DemoTest03_Operator {

	public static void main(String[] args) {
		int a=6;
		int b=2;
		
		
		System.out.println("a + b = "+(a+b));	//加法操作
		System.out.println("a - b = "+(a-b));	//减法操作
		System.out.println("a * b = "+(a*b));	//乘法操作
		System.out.println("a / b = "+(a/b));	//除法操作
		System.out.println("a % b = "+(a%b));	//取模操作
	}
}
